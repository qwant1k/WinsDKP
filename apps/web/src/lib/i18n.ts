function formatTemplate(text: string, params?: Params): string {
  if (!params) return text;
  return Object.entries(params).reduce((acc, [key, value]) => acc.replaceAll(`{{${key}}}`, String(value)), text);
}

export function tr(key: string, params?: Params, localeOverride?: AppLocale): string {
  const locale = localeOverride || useLocaleStore.getState().locale;
  const byLocale = resources[locale] || resources.ru;
  const text = byLocale[key] || resources.ru[key] || key;
  return formatTemplate(text, params);
}

export function useI18n() {
  const locale = useLocaleStore((state) => state.locale);
  const setLocale = useLocaleStore((state) => state.setLocale);

  const t = useMemo(() => {
    return (key: string, params?: Params) => tr(key, params, locale);
  }, [locale]);

  return { locale, setLocale, t };
}

